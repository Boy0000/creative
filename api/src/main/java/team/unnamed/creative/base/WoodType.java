package team.unnamed.creative.base;

/**
 * Represents Minecraft wood type.
 *
 * @since 1.8.0
 */
public enum WoodType {
    /**
     * Oak wood type.
     *
     * @since 1.8.0
     */
    OAK,

    /**
     * Spruce wood type.
     *
     * @since 1.8.0
     */
    SPRUCE,

    /**
     * Birch wood type.
     *
     * @since 1.8.0
     */
    BIRCH,

    /**
     * Acacia wood type.
     *
     * @since 1.8.0
     */
    ACACIA,

    /**
     * Cherry wood type.
     *
     * @since 1.8.0
     */
    CHERRY,

    /**
     * Jungle wood type.
     *
     * @since 1.8.0
     */
    JUNGLE,

    /**
     * Dark oak wood type.
     *
     * @since 1.8.0
     */
    DARK_OAK,

    /**
     * Pale oak wood type.
     *
     * @since 1.8.0
     */
    PALE_OAK,

    /**
     * Mangrove wood type.
     *
     * @since 1.8.0
     */
    MANGROVE,

    /**
     * Bamboo wood type.
     *
     * @since 1.8.0
     */
    BAMBOO,

    /**
     * Crimson wood type.
     *
     * @since 1.8.0
     */
    CRIMSON,

    /**
     * Warped wood type.
     *
     * @since 1.8.0
     */
    WARPED
}
