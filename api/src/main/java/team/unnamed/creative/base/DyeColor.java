package team.unnamed.creative.base;

/**
 * Represents the dye color for (at the moment just) banners.
 *
 * @since 1.8.0
 */
public enum DyeColor {
    /**
     * White color.
     *
     * @since 1.8.0
     */
    WHITE,

    /**
     * Orange color.
     *
     * @since 1.8.0
     */
    ORANGE,

    /**
     * Magenta color.
     *
     * @since 1.8.0
     */
    MAGENTA,

    /**
     * Light blue color.
     *
     * @since 1.8.0
     */
    LIGHT_BLUE,

    /**
     * Yellow color.
     *
     * @since 1.8.0
     */
    YELLOW,

    /**
     * Lime color.
     *
     * @since 1.8.0
     */
    LIME,

    /**
     * Pink color.
     *
     * @since 1.8.0
     */
    PINK,

    /**
     * Gray color.
     *
     * @since 1.8.0
     */
    GRAY,

    /**
     * Light gray color.
     *
     * @since 1.8.0
     */
    LIGHT_GRAY,

    /**
     * Cyan color.
     *
     * @since 1.8.0
     */
    CYAN,

    /**
     * Purple color.
     *
     * @since 1.8.0
     */
    PURPLE,

    /**
     * Blue color.
     *
     * @since 1.8.0
     */
    BLUE,

    /**
     * Brown color.
     *
     * @since 1.8.0
     */
    BROWN,

    /**
     * Green color.
     *
     * @since 1.8.0
     */
    GREEN,

    /**
     * Red color.
     *
     * @since 1.8.0
     */
    RED,

    /**
     * Black color.
     *
     * @since 1.8.0
     */
    BLACK
}
